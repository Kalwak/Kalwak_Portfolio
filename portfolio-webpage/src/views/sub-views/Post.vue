<script>
// post view, shows the blog post contents
import axios from 'axios';
import { formatPostDate } from '@/utils/utils';
import ArticleContent from '@/components/sub-layout/article-content.vue';


// @vuese
export default {
  name: 'post',
  data() {
    return {
      post: {},
      onLoading: true,
    };
  },

  components: {
    ArticleContent
  },

  computed: {
    postUrl() {
      return `${process.env.VUE_APP_API_ENDPOINT}/api/blog/${this.$route.params.id}`;
    },

    postDate() {
      return formatPostDate(this.post.date);
    },

    content() {
      return JSON.parse(this.post.text);
    },

    whatsappShareUrl() {
      return `https://wa.me/?text=${encodeURIComponent(this.pageUrl)}`
    },

    facebookShareUrl() {
      return `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(this.pageUrl)}&t=${encodeURIComponent(this.post.title)}`;
    },

    pageUrl() {
      return window.location.href;
    }
  },

  methods: {
    // @vuese
    // gets backend post data by id
    getPostData() {
      let postUrl = this.postUrl;
      this.onLoading = true;
      axios.get(postUrl)
        .then( response => {
          this.$log.debug(response);
          this.post = response.data;
        })
        .catch( errors => {
          this.$log.debug(errors);
        })
        .finally( () => {
          this.onLoading = false;
        });
    },
  },

  created() {
    this.getPostData();
  },
}
